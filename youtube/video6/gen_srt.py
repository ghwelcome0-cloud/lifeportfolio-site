def ts(s):
    h=int(s//3600); m=int((s%3600)//60); sec=int(s%60); ms=int(round((s-int(s))*1000))
    return f"{h:02d}:{m:02d}:{sec:02d},{ms:03d}"
cues=[]
for key in ["P1","P2","P3","P4","P5"]:
    start,dur,lines = P[key]
    n=len(lines)
    seg = dur/n
    for i,txt in enumerate(lines):
        st = start + i*seg
        en = start + (i+1)*seg - 0.15  # 약간의 여백
        cues.append((st,en,txt))
with open("video6.srt","w",encoding="utf-8") as f:
    for i,(st,en,txt) in enumerate(cues,1):
        f.write(f"{i}\n{ts(st)} --> {ts(en)}\n{txt}\n\n")
print("SRT saved:", len(cues), "cues, last end", round(cues[-1][1],2))
