def ts(s):
    h = int(s // 3600)
    m = int((s % 3600) // 60)
    sec = int(s % 60)
    ms = int(round((s - int(s)) * 1000))
    if ms == 1000:
        ms = 0; sec += 1
    return f"{h:02d}:{m:02d}:{sec:02d},{ms:03d}"

